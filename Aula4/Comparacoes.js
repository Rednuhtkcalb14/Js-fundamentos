// comparações retornam valores booleanos]

// == / ===: Conpara a igualdade

console.log(5 == "5")
console.log(5 === "5")

// == compara apenas os valores
// === compara os valores e os tipos

// ||: operador "ou" usado para quando apenas uma das "afirmações" tem que ser verdadeira

console.log(true || false)

// &&: operaor "e" usado quando todas as "afirmações" devem ser verdadeiras

console.log((true || false) && true)

// != / !==: operadores que verificam a diferença, retornam true apenas se o que
// estiver sendo comparado for diferente
console.log("5" != 5)
console.log("5" !== 5)

